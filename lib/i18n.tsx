const DICTS: Record<'en' | 'es', Record<string, string>> = { en: EN, es: ES }

export const LANGS: { code: Lang; name: string; short: string; html: string }[] = [
  { code: 'pt', name: 'Português', short: 'PT', html: 'pt-BR' },
  { code: 'en', name: 'English',   short: 'EN', html: 'en' },
  { code: 'es', name: 'Español',   short: 'ES', html: 'es' },
]

/** PT é o fallback natural — `t("Cancelar")` retorna "Cancel"/"Cancelar" em EN/ES, ou a própria chave em PT. */
export function useLang() {
  const lang = useUiPrefs((s) => s.lang)
  const setLang = useUiPrefs((s) => s.setLang)
  const t = useCallback(
    (s: string) => (lang === 'pt' ? s : (DICTS[lang]?.[s] ?? s)),
    [lang]
  )
  return { lang, setLang, t }
}

export function useTheme() {
  const theme = useUiPrefs((s) => s.theme)
  const toggle = useUiPrefs((s) => s.toggleTheme)
  return { theme, toggle }
}
